    private void btnLookupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLookupActionPerformed
        // TODO add your handling code here:
        String domain = jTextField1.getText().trim();
        jTextArea2.setText(""); // clear trước khi hiển thị mới

        if (domain.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                    "Please enter a domain or IP address.", 
                    "Input Required", 
                    javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            // ===================== DNS LOOKUP =====================
            StringBuilder result = new StringBuilder();
            result.append("=== DNS Information ===\n");

            java.net.InetAddress inet = java.net.InetAddress.getByName(domain);
            result.append("Host Name: ").append(inet.getHostName()).append("\n");
            result.append("IP Address: ").append(inet.getHostAddress()).append("\n\n");

            // ===================== WHOIS LOOKUP =====================
            result.append("=== WHOIS Information ===\n");

            // Kết nối WHOIS server chuẩn (port 43)
            try (java.net.Socket socket = new java.net.Socket("whois.iana.org", 43);
                 java.io.BufferedWriter bw = new java.io.BufferedWriter(new java.io.OutputStreamWriter(socket.getOutputStream()));
                 java.io.BufferedReader br = new java.io.BufferedReader(new java.io.InputStreamReader(socket.getInputStream()))) {

                bw.write(domain + "\r\n");
                bw.flush();

                String line;
                while ((line = br.readLine()) != null) {
                    result.append(line).append("\n");
                }
            } catch (Exception e) {
                result.append("WHOIS lookup failed: ").append(e.getMessage()).append("\n");
            }

            jTextArea2.setText(result.toString());
        } catch (Exception e) {
            logger.log(java.util.logging.Level.SEVERE, "Lookup error", e);
            javax.swing.JOptionPane.showMessageDialog(this, 
                    "Error performing lookup:\n" + e.getMessage(), 
                    "Lookup Error", 
                    javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnLookupActionPerformed

